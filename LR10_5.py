# –í —Å–ª–µ–¥—É—é—â–∏—Ö ùëÅ —Å—Ç—Ä–æ–∫–∞—Ö –∑–∞–¥–∞–Ω—ã –Ω–æ–º–µ—Ä–∞ —Ü–≤–µ—Ç–æ–≤ –∫—É–±–∏–∫–æ–≤ –ê–Ω–∏. –í –ø–æ—Å–ª–µ–¥–Ω–∏—Ö ùëÄ —Å—Ç—Ä–æ–∫–∞—Ö –Ω–æ–º–µ—Ä–∞ —Ü–≤–µ—Ç–æ–≤ –ë–æ—Ä–∏.
# –ù–∞–π–¥–∏—Ç–µ —Ç—Ä–∏ –º–Ω–æ–∂–µ—Å—Ç–≤–∞:
# –Ω–æ–º–µ—Ä–∞ —Ü–≤–µ—Ç–æ–≤ –∫—É–±–∏–∫–æ–≤, –∫–æ—Ç–æ—Ä—ã–µ –µ—Å—Ç—å –≤ –æ–±–æ–∏—Ö –Ω–∞–±–æ—Ä–∞—Ö;
# –Ω–æ–º–µ—Ä–∞ —Ü–≤–µ—Ç–æ–≤ –∫—É–±–∏–∫–æ–≤, –∫–æ—Ç–æ—Ä—ã–µ –µ—Å—Ç—å —Ç–æ–ª—å–∫–æ —É –ê–Ω–∏ –∏
# –Ω–æ–º–µ—Ä–∞ —Ü–≤–µ—Ç–æ–≤ –∫—É–±–∏–∫–æ–≤, –∫–æ—Ç–æ—Ä—ã–µ –µ—Å—Ç—å —Ç–æ–ª—å–∫–æ —É –ë–æ—Ä–∏.
# –î–ª—è –∫–∞–∂–¥–æ–≥–æ –∏–∑ –º–Ω–æ–∂–µ—Å—Ç–≤ –≤—ã–≤–µ–¥–∏—Ç–µ —Å–Ω–∞—á–∞–ª–∞ –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ —ç–ª–µ–º–µ–Ω—Ç–æ–≤ –≤ –Ω–µ–º, –∞ –∑–∞—Ç–µ–º —Å–∞–º–∏ —ç–ª–µ–º–µ–Ω—Ç—ã, –æ—Ç—Å–æ—Ä—Ç–∏—Ä–æ–≤–∞–Ω–Ω—ã–µ –ø–æ –≤–æ–∑—Ä–∞—Å—Ç–∞–Ω–∏—é.

print("–í–≤–µ–¥–∏—Ç–µ N –∏ M:")
n,m = map(int,input().split())
a = set() #–º–Ω–æ–∂–µ—Å—Ç–≤–æ –¥–ª—è –∫—É–±–∏–∫–æ–≤ –ê–Ω–∏
b = set() #–º–Ω–æ–∂–µ—Å—Ç–≤–æ –¥–ª—è –∫—É–±–∏–∫–æ–≤ –ë–æ—Ä–∏

for i in range(n):
    k = int(input("–í–≤–µ–¥–∏—Ç–µ –Ω–æ–º–µ—Ä–∞ —Ü–≤–µ—Ç–æ–≤ –∫—É–±–∏–∫–æ–≤ –ê–Ω–∏: "))
    a.add(k)

for i in range(m):
    k = int(input("–í–≤–µ–¥–∏—Ç–µ –Ω–æ–º–µ—Ä —Ü–≤–µ—Ç–æ–≤ –∫—É–±–∏–∫–æ–≤ –ë–æ—Ä–∏: "))
    b.add(k)

both = set()#–º–Ω–æ–∂–µ—Å—Ç–≤–æ –¥–ª—è –∫—É–±–∏–∫–æ–≤ –≤ –æ–±–æ–∏—Ö –Ω–∞–±–æ—Ä–∞—Ö
bothlist = []
Ann = set() #–º–Ω–æ–∂–µ—Å—Ç–≤–æ –¥–ª—è –∫—É–±–∏–∫–æ–≤ –ê–Ω–∏
Annlist = []
Borya = set() #–º–Ω–æ–∂–µ—Å—Ç–≤–æ –¥–ª—è –∫—É–±–∏–∫–æ–≤ –ë–æ—Ä–∏
Boryalist = []

#–Ω–æ–º–µ—Ä–∞ —Ü–≤–µ—Ç–æ–≤ –∫—É–±–∏–∫–æ–≤, –∫–æ—Ç–æ—Ä—ã–µ –µ—Å—Ç—å –≤ –æ–±–æ–∏—Ö –Ω–∞–±–æ—Ä–∞—Ö;
both = a.intersection(b)
for elem in both:
    bothlist.append(elem)
bothlist.sort()

# –Ω–æ–º–µ—Ä–∞ —Ü–≤–µ—Ç–æ–≤ –∫—É–±–∏–∫–æ–≤, –∫–æ—Ç–æ—Ä—ã–µ –µ—Å—Ç—å —Ç–æ–ª—å–∫–æ —É –ê–Ω–∏
Ann = a.difference(b)
for elem in Ann:
    Annlist.append(elem)
Annlist.sort()

# –Ω–æ–º–µ—Ä–∞ —Ü–≤–µ—Ç–æ–≤ –∫—É–±–∏–∫–æ–≤, –∫–æ—Ç–æ—Ä—ã–µ –µ—Å—Ç—å —Ç–æ–ª—å–∫–æ —É –ë–æ—Ä–∏.
Borya = b.difference(a)
for elem in Borya:
    Boryalist.append(elem)
Boryalist.sort()

n1 = len(both)
print("–ö–æ–ª–∏—á–µ—Å—Ç–≤–æ –Ω–æ–º–µ—Ä–æ–≤ —Ü–≤–µ—Ç–æ–≤ –∫—É–±–∏–∫–æ–≤,–∫–æ—Ç–æ—Ä—ã–µ –µ—Å—Ç—å –≤ –æ–±—â–∏—Ö –Ω–∞–±–æ—Ä–∞—Ö = ",n1)
for elem in bothlist:
    print(elem)

n2 = len(Ann)
print("–ö–æ–ª–∏—á–µ—Å—Ç–≤–æ –Ω–æ–º–µ—Ä–æ–≤ —Ü–≤–µ—Ç–æ–≤ –∫—É–±–∏–∫–æ–≤,–∫–æ—Ç–æ—Ä—ã–µ –µ—Å—Ç—å —Ç–æ–ª—å–∫–æ —É –ê–Ω–∏ = ",n2)
for elem in Annlist:
    print(elem)

n3 = len(Borya)
print("–ö–æ–ª–∏—á–µ—Å—Ç–≤–æ –Ω–æ–º–µ—Ä–æ–≤ —Ü–≤–µ—Ç–æ–≤ –∫—É–±–∏–∫–æ–≤,–∫–æ—Ç–æ—Ä—ã–µ –µ—Å—Ç—å —Ç–æ–ª—å–∫–æ —É –ë–æ—Ä–∏ = ",n3)
for elem in Boryalist:
    print(elem)